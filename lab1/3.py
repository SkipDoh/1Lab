num1 = int(input("Введите первое целое число: "))
num2 = int(input("Введите второе целое число: "))
num3 = int(input("Введите третье целое число: "))
sum_num = num1 + num2 + num3
prod_num = num1 * num2 * num3
avg_num = sum_num / 3
print("Сумма чисел:", sum_num)
print("Произведение чисел:", prod_num)
print("Среднее арифметическое чисел:", avg_num)